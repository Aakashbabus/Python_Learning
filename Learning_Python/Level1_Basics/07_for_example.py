
# ARRAY IN PYTHON CAN BE DEFINED LIKE THIS
l=['Aakash',5.4,1989]
print(l)

# THERE IS NO NEED TO INCREMENT OR DECREMENT I , IN PYTHON IT DOES ITSELF
for i in l:
    print(i)

# LIST CAN BE PROVIDED IN THE LOOP ITSELF
for i in ['Divya',4.11,1992]:
    print(i)

# SEE HOW STRING IS HANDLED
X= 'APPLE'
for i in X:
    print(i)

# SEE HOW RANGE CAN BE USED
for i in range(1,11):
    print (i)

for i in range(1,11):
    print ('apple')

